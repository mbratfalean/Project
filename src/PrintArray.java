
public class PrintArray {
	public void Print(int[] x, int c) {
		
		for(int i=0;i<=c;i++) {
			System.out.print(x[i]+ " ");
		}
		System.out.println();
	}
      public void Descending(int[] x, int c) {
		
		for(int i=c;i>=0 ;i--) {
			System.out.print(x[i]+ " ");
		}
		System.out.println();
	}

}


